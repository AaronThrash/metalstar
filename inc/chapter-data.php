/**
 * Get chapter by slug
 */
function get_chapter_by_slug($slug) {
    global $chapters;
    foreach ($chapters as $num => $ch) {
        if ($ch['slug'] === $slug) {
            $ch['num'] = $num;
            return $ch;
        }
    }
    return null;
}

/**
 * Get previous/next chapter
 */
function get_adjacent_chapters($current_num) {
    global $chapters;
    return [
        'prev' => isset($chapters[$current_num - 1]) ? $chapters[$current_num - 1] : null,
        'next' => isset($chapters[$current_num + 1]) ? $chapters[$current_num + 1] : null,
    ];
}

/**
 * Estimate reading time
 */
function reading_time($text, $wpm = 225) {
    $words = str_word_count(strip_tags($text));
    $minutes = ceil($words / $wpm);
    return $minutes . ' min read';
}
